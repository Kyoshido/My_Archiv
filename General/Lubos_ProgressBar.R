## vytvářím funkci, která vykresluje lineární progres bar pro lineární
## proceduru ------------------------------------------------------------------

myProgressBar <- function(

    iteration_variable = i,
    n_of_iteration,
    show_percentage = TRUE

){

    flush.console()

    cat(
        "\r",
        paste(paste(rep("%",floor(iteration_variable / n_of_iteration * 50)),collapse = ""
            ),
            paste(
                rep(
                    "_",
                    50 - floor(iteration_variable / n_of_iteration * 50)
                ),
                collapse = ""
            ),
            if(show_percentage){
                paste(
                    " ",
                    format(
                        round(
                            iteration_variable / n_of_iteration * 100,
                            1
                        ),
                        nsmall = 1
                    ),
                    " %",
                    sep = ""
                )
            },
            sep = ""
        )#,"\r"
    )

    cat("\r")

}


## ----------------------------------------------------------------------------

###############################################################################

## jeden příklad za všechny ---------------------------------------------------

for(i in 1:1e4){
    i^2
    myProgressBar(i, 1e4, show_percentage = TRUE)
}


